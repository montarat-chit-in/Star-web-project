        <h3>ส่วนประกอบ (COMPOSION)</h3> 
        <p>   ดาวพุธไม่มีชั้นบรรยากาศห่อหุ้ม และการที่ดาวพุธอยู่ใกล้ดวงอาทิตย์มาก 
            ทำให้อุณหภูมิที่พื้นผิวดาวพุธ ในเวลากลางวันและกลางคืน แตกต่างกันมากถึง 600 องศาเซลเซียส 
            โดยมีอุณหภูมิในเวลากลางวันสูงถึง 430 องศาเซลเซียส และลดลงเหลือ –180 องศาเซลเซียส ในเวลากลางคืน

             โครงสร้างของดาวพุธ
                 ดาวพุธเป็นดาวที่มีเนื้อแข็ง (solid planet) มีแก่น (core)  ปกคลุมด้วยเนื้อดาว  (mantle) 
                 ที่เป็นหินชนิดต่าง ๆ 1 ชั้น โดยมีเปลือกนอก  (crust )  อยู่บนชั้นเนื้อดาวนี้อีกทีหนึ่ง 
                  ดาวพุธก่อเกิดขึ้นเมื่อราว 4,500 ล้านปีมาแล้ว  และเนื่องจากมีขนาเล็กมากจึงกลายเป็นของแข็งได้เร็ว 
                  เชื่อกันว่าบนดาวเคราะห์ดวงนี้ไม่เคยมีพฤติการณ์ภูเขาไฟ (volcanic activity) เกิดขึ้นเลย 
                   ดังนั้นดาวพุธจึงไม่เคยมีการเปลี่ยนแปลงใด ๆ เลยนับแต่ที่ก่อเกิดขึ้นมา 
                   เว้นแต่การถูกพุ่งชนโดยลูกอุกกาบาตต่าง ๆ  (metorites) เท่านั้น </p>
 
