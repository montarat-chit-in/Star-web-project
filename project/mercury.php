        <h3>Mercury</h3>
        <p>  Mercury (ดาวพุธ)

            ดาวพุธเป็นดาวเคราะห์ซึ่งอยู่ใกล้กับดวงอาทิตย์มากที่สุด 
            เป็นดาวเคราะห์ขนาดเล็ก และไม่มีดวงจันทร์บริวาร   โครงสร้างภายในของดาวพุธประกอบไปด้วย 
            แกนเหล็กขนาดใหญ่มีรัศมีประมาณ 1,800 ถึง 1,900 กิโลเมตร ล้อมรอบด้วยชั้นที่เป็นซิลิเกท 
            (ในทำนองเดียวกับที่แกนของโลกถูกห่อหุ้มด้วยแมนเทิลและเปลือก) ซึ่งหนาเพียง 500 ถึง 600 กิโลเมตร 
            บางส่วนของแกนอาจจะยังหลอมละลายอยู่
       
       ​
       
             ดาวพุธถูกเยี่ยมเยือนด้วยยานอวกาศเพียงลำเดียวคือ ยานมารีเนอร์ 10 ในปี พ.ศ. 2517 ซึ่งได้ทำการสำรวจ 
             และทำแผนที่พื้นผิวดาวพุธเป็นครั้งแรก แต่เนื่องจากดาวพุธอยู่ใกล้ดวงอาทิตย์มาก 
             จึงสามารถทำแผนที่ได้เพียงร้อยละ 45 ของพื้นที่ทั้งหมด
       
                ​ </p>

