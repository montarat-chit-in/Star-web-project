        <h3>ส่วนประกอบ (COMPOSION)</h3> 
        <p> ดวงอาทิตย์เป็นดาวฤกษ ์ประเภทดาวแคระเหลือง (yellow dwarf) ดวงหนึ่งจัดเป็นดาวฤกษ์ขนาดย่อม 
             แต่เพราะว่ามันอยู่ห่างจากโลกราว  93  ล้านไมล์ ( 150  ล้านกิโลเมตร)  
             ดวงอาทิตย์จึงเป็นดาวฤกษ์บนฟากฟ้าที่สำคัญที่สุดสำหรับเรา  
             ดวงอาทิตย์เป็นลูกกลมดวงใหญ่ที่ประกอบด้วยก๊าซฮีเลียมประมาณร้อยละ  24  ไฮโดรเจนร้อยละ  75  และธาตุอื่น ๆ 
             อีกประมาณร้อยละ  1 ภายในดวงอาทิตย์มีปฏิกิริยาการหลอมนิวเคลียส (nuclear fusion reactions) ดำเนินอยู่ 
             ส่งผลให้อะตอมของไฮโดรเจนหลอมรวมกันเกิดเป็นอะตอมของฮีเลียมซึ่งมีน้ำหนักมากกว่าเล็กน้อยและให้พลังงานออกมาด้วย  
             พลังงานนี้แผ่ผ่านอวกาศมาถึงโลกทำให้สิ่งมีชีวิตเกิดขึ้นและดำรงอยู่ได้</p>  

