         <h3>โครงสร้าง  (STRUCTURE)</h3>   
         <p>  ดวงอาทิตย์ประกอบขึ้นด้วยมวลก๊าซจำนวนมหาศาลซึ่งทำให้ใจกลาง (core) (1) 
             ซึ่งเป็นส่วนในสุดที่ห้อมล้อมด้วยชั้นที่เย็นกว่าหลายชั้นนั้นร้อนจัดมาก  ที่ใจกลางดังกล่าวมีอุณหภูมิราว 36 ล้านองศาฟาเรนไฮต์
                แต่ที่ผิวนอกร้อนเพียง  11,000  องศาเท่านั้น ตรงส่วนบนสุดของใจกลางเป็นเขตการแผ่รังสี ( radiant zone)  (2) 
                 ซึ่งปลดปล่อยรังสีคลื่นแม่เหล็กไฟฟ้าออกมาภายนอก ถัดไปเป็นเขตการพา(convection zone) (3)
                  ซึ่งเป็นที่ที่มีลำก๊าซมหิมาจำนวนมากผุดพลุ่งขึ้นและยุบลงสลับกัน 
                   ถัดออกมาก็เป็นผิวนอกของดวงอาทิตย์ที่เรามองเห็นได้และรู้จักกันในชื่อของโฟโตสเฟียร์ (photosphere) (4) 
                   ซึ่งเป็นชั้นบาง ๆ เพียง ชั้นเดียว

                ​
                
             บนชั้นโฟโตสเฟียร์นี้ยังมีชั้นบาง ๆ อีก 1 ชั้นเรียกว่า โครโมสเฟียร์ (chromosphere)  
             ซึ่งหนาประมาณ 1,800 ไมล์ (3,000 กิโลเมตร) ถัดออกมาเป็นชั้นของก๊าซในสภาพเป็นไอออนที่มีความหนาแน่นต่ำ
             และร้อนจัดมากพวยพุ่งออกมาจากดวงอาทิตย์เป็นระยะทางไกลซึ่งทำให้เห็นเป็นวงแสงสีรุ้งรอบดวงอาทิตย์เมื่อเกิดสุริยุปราคา 
              ชั้นของก๊าซนี้เรียกว่ากลดสุริยะ (solar corona)  เป็นชั้นที่ร้อนจัดมากชั้นหนึ่ง ทั้ง  2 ชั้นนี้ถือได้ว่าเป็นชั้นบรรยากาศ
              ของดวงอาทิตย์ (the Sun's atmoshere)
                
