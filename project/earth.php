        <h3>ส่วนประกอบ (COMPOSION)</h3> 
        <p> ชั้นบรรยากาศของโลกประกอบไปด้วยไนโตรเจน 77 % ออกซิเจน 21% ที่เหลือเป็น 
            อาร์กอน คาร์บอนไดออกไซด์ และน้ำ ก๊าซคาร์บอนไดออกไซด์ช่วยในการกักเก็บความร้อนไว้ภายใต้ชั้นบรรยากาศ 
            โดยอาศัยสภาวะเรือนกระจก ทำให้โลกมีความอบอุ่น ไม่หนาวเย็นจนเกินไปสำหรับสิ่งมีชีวิต อย่างไรก็ตาม 
            ถ้าปริมาณก๊าซคาร์บอนไดออกไซด์เพิ่มขึ้นมากจนเกินไป ก็จะทำให้โลกร้อนขึ้น 
            ซึ่งอาจส่งผลให้สิ่งมีชีวิตไม่สามารถดำรงอยู่ได้เช่นกัน

                ​    
          นอกจากนี้ โลกยังมีสนามแม่เหล็ก ที่เกิดจากการหมุนรอบตัวเองของโลก
           ถึงแม้ว่าจะมีความเข้มไม่มากนัก แต่ช่วยปกป้องมิให้อนุภาคที่มีพลังงานสูงจากอวกาศเดินทางผ่านมาที่ผิวโลกได้ 
           โดยสนามแม่เหล็กจะกักให้อนุภาค เดินทางไปตามเส้นแรงแม่เหล็ก และเข้าสู่ชั้นบรรยากาศได้ เพียงที่ขั้วโลกเหนือ 
           และขั้วโลกใต้เท่านั้น ซึ่งเป็นตำแหน่งของขั้วแม่เหล็กโลกนั่นเอง เมื่ออนุภาคเหล่านี้ทำปฏิกิริยากับโมเลกุลก๊าซในชั้นบรรยากาศ 
           ทำให้เกิดแสงสีสวยงาม สังเกตเห็นบนท้องฟ้ายามค่ำคืน เรียกว่า "แสงเหนือแสงใต้" (Aurora) ตามบริเวณที่ปรากฏนั่นเอง</p>  

