        <h3>Venus</h3>
        <p> Venus (ดาวศุกร์)

                ดาวศุกร์อยู่ห่างจากดวงอาทิตย์เป็นลำดับที่ 2 เป็นดาวเคราะห์ที่มีขนาดใหญ่เป็นอันดับที่ 6 และไม่มีดวงจันทร์บริวาร 
            ดาวศุกร์มีลักษณะที่คล้ายคลึงกับโลก จนได้ชื่อว่าเป็นดาวเคราะห์ฝาแฝดกับโลกของเรา โครงสร้างภายในของดาวศุกร์ 
            ประกอบด้วย แกนกลางที่เป็นเหล็กมีรัศมีประมาณ 3,000 กิโลเมตร ห่อหุ้มด้วยชั้นแมนเทิลที่มีความหนาประมาณ 3,000 กิโลเมตร 
            และเปลือกแข็งที่ประกอบด้วยหินซิลิเกต
   
   ​
             ยานอวกาศลำแรกที่เดินทางไปดาวศุกร์คือ มาริเนอร์ 2 ใน พ.ศ.2505  หลังจากนั้นก็มีอีกหลายลำ จนกระทั่งในปี พ.ศ. 2532 ยานอวกาศแมกเจลแลน 
             ได้ใช้เรดาร์ในการสำรวจผ่านชั้นบรรยากาศที่หนาแน่นของดาวศุกร์ เพื่อทำแผนที่พื้นผิวของดาว การสำรวจโดยใช้สัญญาณเรดาร์ ทำให้ทราบถึงความสูงต่ำของพื้นผิวดาวศุกร์ได้ 
             และพบว่าพื้นผิวดาวศุกร์ปกคลุมไปด้วย ที่ราบที่เกิดจากการระเบิดของภูเขาไฟ และภูเขาไฟขนาดใหญ่หลายแห่ง นอกจากนี้ยังพบว่า พื้นผิวดาวศุกร์ไม่มีหลุมอุกกาบาตขนาดเล็กกระจายอยู่ทั่วไป 
             เช่นเดียวกับดาวพุธ ทั้งนี้เป็นเพราะว่า อุกกาบาตจะถูกเผาไหม้ไปจนหมด ในระหว่างที่เดินทางเข้าสู่ชั้นบรรยากาศที่หนาแน่นของดาวศุกร์ 
                ​ </p>
