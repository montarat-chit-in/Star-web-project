        <h3>ชั้นบรรยากาศ</h3> 
        <p>     ชั้นบรรยากาศของดาวศุกร์นั้นมีความหนาแน่นมาก ความดันบรรยากาศที่พื้นผิวดาวศุกร์มีค่าประมาณ 90 เท่า ของความดันบรรยากาศที่พื้นผิวโลก 
            หรือมีค่าเท่ากับความดันที่ใต้ทะเลลึก 1 กิโลเมตร บรรยากาศของดาวศุกร์ประกอบไปด้วย ก๊าซคาร์บอนไดออกไซด์เป็นส่วนใหญ่ 
            และมีชั้นเมฆอยู่หลายชั้นที่ประกอบไปด้วยก๊าซซัลเฟอร์ไดออกไซด์ (กรดกำมะถัน) ซึ่งมีความหนาหลายกิโลเมตร ทำให้เราไม่สามารถสังเกตเห็นพื้นผิวดาวศุกร์ได้

            ​
            
                 ชั้นบรรยากาศที่หนาทึบนี้ทำให้เกิดสภาวะเรือนกระจก กักเก็บความร้อนไว้ภายใต้ชั้นบรรยากาศ ทำให้อุณหภูมิพื้นผิวสูงถึง 600-900 องศาเซลเซียส 
                 จะเห็นว่าพื้นผิวดาวศุกร์ร้อนกว่าพื้นผิวดาวพุธมาก ทั้งๆ ที่อยู่ไกลจากดวงอาทิตย์กว่าดาวพุธถึงสองเท่าก็ตาม
            
                 โครงสร้างของดาวศุกร์ (STRUCTURE OF THE PLANET)
            
            
                       ดาวศุกร์เป็นดาวเคราะห์ที่มีเนื้อแข็ง  ส่วนแก่น (core)  มีลักษณะคล้ายโลหะ  ล้อมด้วยชั้นของเนื้อดาว (mantle) 
                       ที่มีลักษณะเป็นหินหนามาก  โดยมีเปลือกนอ (crust) บาง ๆ อยู่บนชั้นเนื้อดาวอีกทีหนึ่ง  จากการสำรวจอวกาศ   (space probe) 
                       ที่ประสบความสำเร็จในการลงสู่พื้นดาวศุกร์ พบว่าดาวศุกร์มีพื้นที่ราบกว้างขวางหลายแห่ง  มีหุบเขามากมาย และมีภูเขาที่มีความสูงกว่า 32,800  ฟุต (10,000  กิโลเมตร)  
                       อยู่หลายลูก  ส่วนบริเวณที่เห็นลึกลงไปนั้นในอดีตน่าจะเป็นทะเลต่าง ๆ  ซึ่งมีมาก่อนการก่อเกิดขึ้นของบรรยากาศ </p>
 
